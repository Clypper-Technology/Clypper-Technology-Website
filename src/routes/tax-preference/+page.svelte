            <h1>The Most Customisable and User-Friendly Tax Toggle and Tax Preference Plugin for WooCommerce</h1>

            <p><strong>Tax Preference for WooCommerce</strong> allows your shop visitors to view prices with or without tax. UI elements are fully customizable, and the toggle button can be placed anywhere on the page using shortcodes, widgets, or PHP.</p>

            <h2>Key Features:</h2>
            <ul>
                <li><strong>Intuitive Toggle</strong> - Display a user-friendly toggle that switches product prices to display with tax or without tax.</li>
                <li><strong>Fully Customisable</strong> - All text and color can be customized by the shop owners. No matter how you choose to display the toggle.</li>
                <li><strong>Popup on First Visit</strong> - Display a customized popup to visitors. Allows the user to choose tax preference before entering the shop.</li>
                <li><strong>Dynamic Price Toggling</strong> - Dynamically switches tax for product prices without refreshing the page.</li>
                <li><strong>Compatible with AJAX Plugins</strong> - Works alongside AJAX-based product search and filter plugins.</li>
                <li><strong>Integrates with Your Favorite Page Builder</strong> - Dynamic widget that integrates with popular page builders like Oxygen, Bricks, and Elementor.</li>
                <li><strong>Display Anywhere on the Page</strong> - You can display the toggle through shortcodes, widgets, and PHP.</li>
                <li><strong>Optimized for Performance</strong> - Developed with minimal JavaScript and CSS, making the impact on site performance minimal.</li>
            </ul>

            <p>Choosing a tax preference for a user is as simple as clicking a toggle button that site owners can place anywhere on the page. A user's chosen tax preference is saved, and navigating to new pages does not reset the tax preference.</p>

            <p>The plugin also provides you with the option to use a popup that allows the user to choose their preferred tax option when first visiting your site.</p>

            <h2>Usage</h2>
            <p>The tax toggle for Tax Switcher can be placed anywhere on your store. Use one of the following methods to display the tax toggle:</p>
            <ol>
                <li><strong>Dynamic Widget</strong> - Enables you to place one or more tax toggles on your page. Allows you to style each toggle individually.</li>
                <li><strong>Shortcode</strong> - Use the shortcode <code>[tax-toggle]</code> to insert the tax toggle. Change the text and style from the plugin settings.</li>
                <li><strong>PHP</strong> - Use PHP by calling the function <code>tax_toggle_tax_shortcode()</code> or using the function <code>&lt;?php echo do_shortcode('[tax-toggle]'); ?&gt;</code>. Change the text and style from the plugin settings.</li>
            </ol>

            <h2>Compatibility</h2>
            <p><strong>This plugin is not compatible with plugins that alter the way in which WooCommerce displays prices on the shop.</strong></p>
            <p><strong>Tested and fully compatible with WordPress 6.*+ and WooCommerce 9.*+, and PHP 7.4+.</strong></p>

            <h2>Settings Overview</h2>

            <h3>Widget Settings</h3>
            <ul>
                <li><strong>Excl. VAT Button Text</strong> - Text for the Excl. VAT button.</li>
                <li><strong>Incl. VAT Button Text</strong> - Text for the Incl. VAT button.</li>
                <li><strong>Toggle Background Color</strong> - Select the background color for the tax toggle.</li>
                <li><strong>Active Toggle Element Color</strong> - Select the color for the active toggle element.</li>
                <li><strong>Toggle Text Color</strong> - Select the color of the text in the tax toggle.</li>
            </ul>

            <h3>Label Settings</h3>
            <ul>
                <li><strong>Tax Included Label</strong> - The text displayed next to prices when tax is included (e.g., 'incl. VAT').</li>
                <li><strong>Tax Excluded Label</strong> - The text displayed next to prices when tax is not included (e.g., 'excl. VAT').</li>
                <li><strong>Zero Tax Label</strong> - Text to show for products exempt from tax.</li>
                <li><strong>Show Tax Suffixes in Cart</strong> - Check this box to show tax suffixes (e.g., 'incl. VAT', 'excl. VAT') next to prices and subtotal in the cart.</li>
            </ul>

            <h3>Variable Product Settings</h3>
            <ul>
                <li><strong>Variable Price Prefix</strong> - Prefix for displaying variable product prices (e.g., 'From:').</li>
            </ul>

            <h3>Popup Settings</h3>
            <ul>
                <li><strong>Enable Popup</strong> - Enable a popup prompting the user to select a tax preference when required.</li>
                <li><strong>Popup Background Color</strong> - Select the background color for the popup.</li>
                <li><strong>With Tax Button Color</strong> - Select the color for the with tax button.</li>
                <li><strong>With Tax Button Text Color</strong> - Choose the text color for the 'With Tax' button.</li>
                <li><strong>Without Tax Button Color</strong> - Select the color for the without tax button.</li>
                <li><strong>Without Tax Button Text Color</strong> - Select the color of the without tax button text color.</li>
                <li><strong>Popup Header</strong> - The header text for the tax selection popup.</li>
                <li><strong>Popup Description</strong> - Additional text to explain the choice in the tax selection popup.</li>
                <li><strong>Business Button Text</strong> - Text for the button used by business customers to indicate no tax is to be added.</li>
                <li><strong>Private Button Text</strong> - Text for the button used by private customers to include tax in prices.</li>
            </ul>

            <h2>Installation</h2>
            <ol>
                <li><strong>Download the Plugin</strong> - Start by downloading the .ZIP file of "Tax Switcher for WooCommerce" plugin.</li>
                <li><strong>Upload and Activate</strong> - Log in to your WordPress admin dashboard. Navigate to <code>Plugins</code> -> <code>Add New</code> -> <code>Upload Plugin</code>. Choose the downloaded .ZIP file and click <strong>Install Now</strong>. Once installed, activate the plugin.</li>
                <li><strong>Enable Tax Rates and Calculations</strong> - Go to <code>WooCommerce</code> -> <code>Settings</code> -> <code>General</code>. Ensure that the "Enable tax rates and calculations" option is checked to allow tax handling by the plugin.</li>
                <li><strong>Configure Default Tax Rate</strong> - Set up a default tax rate for your store. Access <code>WooCommerce</code> -> <code>Settings</code> -> <code>Tax</code> -> <code>Standard Rates</code>. Here, you can input your tax rates according to your store's requirements.</li>
                <li><strong>Customize Plugin Settings</strong> - After activation, customize the plugin to match your store's style and preferences. Visit the plugin settings page to adjust colors, texts, and other features as needed.</li>
            </ol>

